学会学习[译] Learning to learn
<p>翻译自john.sonmez的<a href="http://simpleprogrammer.com/2012/09/23/learning-to-learn/">文章Learning to learn</a></p>
<h2>关键点</h2>
<p>我相信对我帮助最大并且我会坚持做下去的事，就是我学会怎样学习一个东西，我的自学能力。
我发现只有当你掌控学习过程的主动权，<strong>学习的真正目的：将知识用于与实际，才能实现</strong>
<strong>那什么是学会学习呢？</strong>
根本上说，就是找到学习一个特定东西的方法。你可以用这个和传统靠他人教你的学习模型作对比。
社会上普遍认为接受当前的教育体系是学习东西的正确方法。学校教育可能很好很重要，但是通常不是掌握有用知识的最好方法。
一个叫做Herbert Spencer的聪明人，是19世纪中期英国的哲学家曾经说过：</p>
<div class="quote">“教育的目的不是知识而是实际应用。”</div>
<p>我不是说学习需要别人教，也许你看着篇博文就是因为你想学习一些东西。
我的观点很简单，学习对你有用东西的最有效方法就是找出哪些是你必须学的并且应该怎样自己学，而不是从别人那里拿来完整的处方，别人也许能把东西分解帮助你学习，但是你最终要为你自己的学习负责。
花一点时间和我一起说，我认为这很重要。</p>
<div class="quote">“我为我自己的学习负责。”</div>
<p>这是一句很激励人心的话。当你真正理解它，你开始发现没有人能给你一个分数，除了你自己。（我并不是像“每个孩子都很特别，每个人都努力过，因此给一些孩子A或者F打击孩子自尊是不公平的”这句话这种意思）
我是说，即使你全得A在大学得到4.0的GPA，你最终也要自己评判你是否学到了东西，或者你只是完成了考试。
只有当你掌控学习过程的主动权，你才能达到学习的真正目的：将知识用于实际。
</p>
<h2>为什么学会学习如此重要</h2>
<p>你曾想过学习有多昂贵吗？ 大学有没有一种让教育比你自学更好的魔法公式？
当你考虑你花在传统教室教育的金钱和时间，你真要问“你是不是从你宝贵的资源中获得了最大的好处？”
我想大多数时间答案是否定的。
系统教育的问题在于它不够有效。学习的过程是非常个性的。不像一些很容易提炼应用的东西就像贴在求知的学生额头的膏药。
不同的人不仅学习的方式不同，而且对他们重要的东西也不同，因此他们的学习差异很大。
让我们在这里坦白说，在大多数教育系统里你做的不过是吃掉东西再反胃出来，并不是真正学习他们。也许你能记住他们足够久来通过考试，或者毕业进入更高一层的领域，但是你是不是学到了教科书上教的大多数东西？你真的需要吗？
总的来说，在传统的填鸭式教育下，你通常不能让你的金钱和时间花的值。
我仍然希望我们都赞成教育很重要。
真因为教育如此重要我们不想浪费我们的金钱和时间获得它，学会学习很重要。
当你有教自己你所需要的知识的能力的时候，你立即失去了约束，你将自己绑定在独有的知识区域或者技术方面。
<strong>当你能比教育组织更有效教你自己知识的时候，你也许得到了一个人一生中最有价值的一个礼物，人生会取得成功。</strong>
你让自己有能力只做你想做的事。（当然在时空和物理现实的限制下）
一旦你有了这种能力，你也会发现你更好教别人你知道的东西。
由于世界上大多数人并不擅长这个，你有真正供不应求的价值。如果你选定一个东西，找出学习它的方法，并且做得很成功，你能帮助那些没有掌握这种能力的人。
</p>
<h2>怎样做</h2>
<p>如果我们没有真正学会怎样学会学习，上面谈论的学会学习就毫无价值。
比标题更准确的说这部分讲学会怎样学会学习，我觉得用“怎样做”比较接近的意思。
够了多嘴的人，让我们继续。
<strong>审视这个东西</strong>
学习一个东西的第一步也许是最关键的一步是审视这个你想学的东西的范围。
很多人跳过了这步漫无目的地在高楼大厦间穿梭，从来不知道他们要找什么。
在审视你想学的东西中，考虑最初的粒度很重要。你想学习的东西的粒度对你消化整个东西很重要。
明确地说，你不能详细学习一个很大的东西。（至少在实际允许的时间内）
你只能在学习的细致程度和范围上取得平衡。
例如，由于这是一个编程博客，假设你要学习一个特定的技术，如C#。
你可以用排笔学习这种语言的基本知识，大体学会怎样构造逻辑语句然后用那种语言编程。
你也可以决定你要学习究竟C#是怎样工作的，每个关键字在特定环境下是怎样表现的。这种细节的程度，当然能在<a href="http://go.microsoft.com/fwlink/?LinkId=199552">C# language specification。</a>（如果你没有点链接，那是一个505页的书，有所有C#语言的技术细节。）
当然你能如此细节学习这种语言，基于为什么你要学习它选择C#的一个特定方面详细学习可能要比学习C#的每个方面每种情况要好的多。
再举另一个简单的例子。如果你想学习世界历史，你要么对整个世界的历史进行高层次概括的学习，或者选择一个特定的时期和地点详细学习。
